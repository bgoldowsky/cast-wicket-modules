package org.cast.cwm.data;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Inheritance;
import javax.persistence.InheritanceType;

import lombok.AccessLevel;
import lombok.Getter;
import lombok.Setter;
import lombok.ToString;

import org.hibernate.annotations.Cache;
import org.hibernate.annotations.CacheConcurrencyStrategy;
import org.hibernate.annotations.GenericGenerator;
import org.hibernate.annotations.Index;

/**
 * Record of running database initialization code.
 * This is used to avoid running certain initializations twice.
 * @see {@link org.cast.cwm.data.init.IDatabaseInitializer}
 * @see {@link org.cast.cwm.CwmApplication}
 * 
 * @author bgoldowsky
 *
 */
@Entity
@Inheritance(strategy = InheritanceType.SINGLE_TABLE)
@Cache(usage = CacheConcurrencyStrategy.NONSTRICT_READ_WRITE)
@GenericGenerator(name="my_generator", strategy = "org.cast.cwm.CwmIdGenerator")
@Getter
@Setter
@ToString(of={"id", "name"})
public class Initialization extends PersistedObject {
  
	private static final long serialVersionUID = 1L;
	
	@Id @GeneratedValue(generator = "my_generator")
	@Setter(AccessLevel.NONE) 
	private Long id;
	
	@Column(nullable=false)
	@Index(name="initialization_name_idx")
	private String name;
	
	@Column(nullable=false)
	private Date runDate;
	
	public Initialization() { /* No Arg Constructor for Datastore */ }

}